    private void updateData(int itemCode, String itemName, String itemType, int itemPrice, int itemQuantity, int itemTolaQauntity) {
        Connection conn = null;
        PreparedStatement ps = null;
        int result = 0;
        String updateQuery = "UPDATE items SET  Name =? , Type =? , Price =? , Quantity =? , TotalPrice =?  WHERE Code = '" + itemCode + "' ";

        try {
            conn = DatabaseConnection.getconnection();
            ps = conn.prepareStatement(updateQuery);
            ps.setString(1, itemName);
            ps.setString(2, itemType);
            ps.setInt(3, itemPrice);
            ps.setInt(4, itemQuantity);
            ps.setInt(5, itemTolaQauntity);
            result = ps.executeUpdate();
            if (result != 0) {
                JOptionPane.showMessageDialog(null, "تم عمليه تعديل الصنف ");
            } else {
                JOptionPane.showMessageDialog(null, "هناك خطا في عمليه التعديل ");
            }
        } catch (SQLException ex) {
            Logger.getLogger(data.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                if (conn != null) {
                    conn.close();
                }

                if (ps != null) {
                    ps.close();
                }
            } catch (Exception e) {
            }
        }

    }

    private void deleteRecord(int itemCode) {
        Connection conn = null;
        PreparedStatement ps = null;
        int result = 0;
        String deleteQuery = "DELETE FROM  items  WHERE Code = '" + itemCode + "'";

        try {
            conn = DatabaseConnection.getconnection();
            ps = conn.prepareStatement(deleteQuery);
            result = ps.executeUpdate();
            if (result != 0) {
                JOptionPane.showMessageDialog(null, "تم عمليه حذف الصنف ");
            } else {
                JOptionPane.showMessageDialog(null, "هناك خطا في عمليه التعديل ");
            }
        } catch (SQLException ex) {
            Logger.getLogger(data.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                if (conn != null) {
                    conn.close();
                }

                if (ps != null) {
                    ps.close();
                }
            } catch (Exception e) {
            }
        }

        Listcanf list = new Listcanf();
        list.setVisible(true);
        this.dispose();
    }

