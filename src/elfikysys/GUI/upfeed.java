    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        DefaultTableModel t = (DefaultTableModel) jTable1.getModel();
        feedback m = new feedback();
        m.load("feedback.bin");
        for (int i = jTable1.getRowCount() - 1; i >= 0; i--) {
            t.removeRow(i);
        }

        String start = String.valueOf(cmb6.getSelectedItem()) + "/" + String.valueOf(cmb5.getSelectedItem()) + "/" + String.valueOf(cmb4.getSelectedItem());
        String end = String.valueOf(cmb9.getSelectedItem()) + "/" + String.valueOf(cmb8.getSelectedItem()) + "/" + String.valueOf(cmb7.getSelectedItem());
        int f = 0;
        double sum = 0;
        DecimalFormat df = new DecimalFormat();
        df.setMaximumFractionDigits(2);
        for (int xx = 0; xx < m.ar.size(); xx++) {

            
            String date = String.valueOf(m.ar.get(xx).getDate().substring(0, m.ar.get(xx).getDate().length() - 9));
            if (date.equalsIgnoreCase(start)) {
                f = 1;
            }
            if (f == 1) {

                t.addRow(new Object[]{m.ar.get(xx).getDate(), df.format(Double.parseDouble(m.ar.get(xx).getPrice())), m.ar.get(xx).getEnwan()});
                sum = sum + Double.parseDouble(m.ar.get(xx).getPrice());
            }

            if (String.valueOf(m.ar.get(xx).getDate().substring(0, m.ar.get(xx).getDate().length() - 9)).equalsIgnoreCase(end)) {
                if (xx == m.ar.size() - 1) {
                    break;
                } else if (!String.valueOf(m.ar.get(xx+1).getDate().substring(0, m.ar.get(xx+1).getDate().length() - 9)).equalsIgnoreCase(end)) {
                    break;
                }
            }
        }
        jLabel4.setText(String.valueOf(df.format(sum)));
    }//GEN-LAST:event_jButton5ActionPerformed

