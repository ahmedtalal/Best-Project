    void fun() {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        feedback c = new feedback();
        c.load("feedback.bin");
        double sum = 0;
        for (int i = model.getRowCount() - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        DecimalFormat df = new DecimalFormat();
        df.setMaximumFractionDigits(2);
        for (int i = 0; i < c.ar.size(); i++) {
            sum = sum + Double.parseDouble(c.ar.get(i).getPrice());
            model.addRow(new Object[]{c.ar.get(i).getDate(), Double.parseDouble(c.ar.get(i).getPrice()), c.ar.get(i).getEnwan()});
        }

        jLabel4.setText(String.valueOf(df.format(sum)));
    }

